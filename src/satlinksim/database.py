def init_db(db_path=None):
    if db_path is None:
        db_path = os.path.join(os.path.dirname(__file__), "satellites.db")
    
    conn = sqlite3.connect(db_path)
    cur = conn.cursor()

    cur.execute("""
    CREATE TABLE IF NOT EXISTS satellites (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT,
        norad_id INTEGER UNIQUE,
        tle_line1 TEXT,
        tle_line2 TEXT
    )
    """)

    lines = [l.strip() for l in tle_text.splitlines() if l.strip()]

    for i in range(0, len(lines), 3):
        name = lines[i]
        l1 = lines[i+1]
        l2 = lines[i+2]
        norad_id = int(l1[2:7])

        cur.execute(
            "INSERT OR IGNORE INTO satellites (name, norad_id, tle_line1, tle_line2) VALUES (?, ?, ?, ?)",
            (name, norad_id, l1, l2)
        )

    conn.commit()
    conn.close()

if __name__ == "__main__":
    init_db()
